function display(data)
{
    products.innerHTML=null

    data.forEach(function(elem){
        var card=document.createElement("div")
        card.style.height="400px"
        card.style.width="215px"
        card.style.padding="10px"
        card.style.border="1px solid rgb(187, 187, 187)"
       


        var img = document.createElement("img")
        img.setAttribute("src",elem.img)
        img.style.display="flex"
       
        var category = document.createElement("p")
        category.textContent=elem.category;
        
        var category = document.createElement("p")
        category.textContent=elem.category;

        var name=document.createElement("h4")
        name.innerText=elem.name
    
        var des=document.createElement("h4")
        des.innerText=elem.des
    
        var cprice=document.createElement("h3")
        cprice.innerText=elem.cprice
        

        var price=document.createElement("h4")
        price.innerText=elem.price
        price.style.color="red"
      
        var button=document.createElement("button")
        button.innerText="Add to card"
        
        button.addEventListener("click",function(){
            AddToCard(elem)
        })


        var box=document.createElement("div")
        box.setAttribute("id","box")

        box.append(cprice,price)

        card.append(img,name,des,box,button)
        products.append(card)
    
    })
}

